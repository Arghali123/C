/*
free(void *ptr): Frees the memory previously allocated by malloc, calloc, or realloc.

1.)“free” method in C is used to dynamically de-allocate the memory.
2.)The memory allocated using functions malloc() and calloc() is not de-allocated on their own.
3.)Hence the free() method is used, whenever the dynamic memory allocation takes place.
It helps to reduce wastage of memory by freeing it.
*/